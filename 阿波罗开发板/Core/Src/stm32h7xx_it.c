/**
  * @brief This function handles Hard fault interrupt.
  */
void HardFault_Handler(void)
{
  /* USER CODE BEGIN HardFault_IRQn 0 */
  /* 检查栈指针并提取PC */
  uint32_t *stack_ptr;
  if ((__get_CONTROL() & 2) == 0) {
    stack_ptr = (uint32_t *)__get_MSP(); /* 主栈 */
  } else {
    stack_ptr = (uint32_t *)__get_PSP(); /* 进程栈 */
  }
  
  /* 栈中第7个位置是PC（R0,R1,R2,R3,R12,LR,PC,xPSR） */
  uint32_t pc = 0;
  if ((uint32_t)stack_ptr >= 0x20000000 && (uint32_t)stack_ptr < 0x20080000) {
    pc = stack_ptr[6];
  }
  
  printf("[HardFault] PC=0x%08lX, CFSR=0x%08lX\r\n", pc, SCB->CFSR);

  /* USER CODE END HardFault_IRQn 0 */
  while (1)
  {
    /* USER CODE BEGIN W1_HardFault_IRQn 0 */
    /* USER CODE END W1_HardFault_IRQn 0 */
  }
}

